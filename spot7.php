<h4>PAADJAO FALLS</h4>

    <img src="images/paadjao.jpg">

<p>While Marinduque is well-known for its beautiful beaches, one enchanting waterfall stands out from the rest of the Mogpog Marinduque tourist attractions: the Paadjao Falls. The Paadjao Falls are located in Mogpog municipality and are only a 10-minute walk from the highway, which is only 5.5 kilometers away.</p>
<p>The hidden cascade flows gently from a 15-foot-tall rocky cliff down to a 6-foot-deep basin filled with cold, jade-green water. It gracefully nestles beneath the cool shade of verdant greenery and tall trees, bringing you closer to nature. Huge and edgy rocks surround the falls, so be extra cautious when crossing the river and falls.</p>
<p>Paadjao Falls flows into the Mogpog River downstream. It is also an excellent picnic location where guests can pitch tents for camping. Apart from Paadjao Falls, other lesser-known waterfalls in Marinduque include Norada Falls, Kawa Kawa Falls, and Bulusukan Falls.</p>

