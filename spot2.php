    <h4>POCTOY WHITE BEACH</h4>
    <img src="images/poctoy.jpg">

<p>The Poctoy White Beach, a long stretch of fine white sand lined with palm trees in the coastal municipality of Torrijos, is one of Marinduque's most well-known attractions. The powdery beach is surrounded by crystal clear waters that are ideal for swimming and kayaking. Beachfront huts and cottages with chairs and tables are ideal for relaxing afternoons on the beach.</p>
<p>Scuba divers will marvel at the sight beneath the crystalline waters. The Poctoy Underwater Museum displays massive Moryon monuments, which represent Marinduque's iconic Moriones festival, which takes place every Holy Week.</p>
<p>Aside from being one of the most beautiful beaches in Marinduque, Poctoy White Beach is one of the newest Torrijos Marinduque tourist destinations. Because of its pristine shores and panoramic sunset views glowing over the horizon, it attracts a large number of local and international visitors each year.</p>

