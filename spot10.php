<h4>MOLBOG SULFUR SPRING</h4>

    <img src="images/molbog.jpg">

<p>Malbog Sulfur Spring is a natural hot spring located at the base of Mount Malindig. It is a stratovolcano in the town of Buenavista, rising 1,157 meters above sea level.</p>
<p>Malbog Sulfur Spring is nestled among lush greenery and verdant flora and fauna. Its warm, bluish-white sulfuric water is thought to have therapeutic and healing properties. The spring has two swimming pools, both of which have medicinal properties that can be used to treat skin diseases.</p>
<p>The stream flows directly from the mountain and flows into a spring pool where tourists can cool off. Its water is safe to drink and is thought to cure both acute and chronic skin ailments. Some visitors even bring bottles of sulfuric water with them on their way home.</p>

