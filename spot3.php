  <h4>TRES REYES ISLANDS</h4>
    <img src="images/tresreyes.jpg">

<p>The Treys Reyes Islands, located off the coast of Gasan municipality, are another postcard-worthy spot in Marinduque. The starting point is Gasan port, where tour boats await passengers before departing for the nearby Three Kings islets of Melchor, Gaspar, and Baltazar.</p>
<p>Gaspar Island is the largest of the three, a tranquil paradise with crystal clear waters and fine white sand. Melchor Island follows, with its lush vegetation covering rock formations. Finally, Baltazar Island is a lovely haven for those looking for a secluded spot to lounge on the sand and in beach cabanas.</p>
<p>A hilltop lighthouse built during the American colonial period in 1910 is one of the must-see attractions on Baltazar Island. Don't forget to see the unique rock formations at Kulapnit Cave before concluding your island adventure. Then, pay a visit to Kay Bungo Cave, a historical site where elongated skulls were discovered.</p>

